function currentLocale() {
    return i18n.global.locale.value
}

export function translateText(text: unknown): string {
    if (text === undefined || text === null) return ""
    const source = String(text)
    if (currentLocale() === "zh-CN") return source
    return enUS[source] || source
}

export function translateCategoryName(category?: CategoryEntity | null): string {
    if (!category) return ""
    if (currentLocale() === "zh-CN") return category.name
    return categoryByNameEn[category.name_en] || category.name_en || category.name
}

export function translateCategoryByNameEn(nameEn: string, fallback = ""): string {
    if (currentLocale() === "zh-CN") return fallback
    return categoryByNameEn[nameEn] || fallback || nameEn
}

export function translateTemplateText(text: string): string {
    return text.replace(/[\u4e00-\u9fff][\u4e00-\u9fff A-Za-z0-9，。“”！？：、（）()\-]+/g, match => translateText(match))
}
